    public void readJUsers(){
         DefaultTableModel modelo =(DefaultTableModel) jUsers.getModel();
         UsuarioDAO usuarioDAO = new UsuarioDAO();
         for(Usuario u : usuarioDAO.read()){
             modelo.addRow(new Object[]{
                 u.getNome(),
                 u.getCpf(),
                 u.getSaldo()
             });
             
         }
    }
